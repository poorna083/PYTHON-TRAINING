

## 📝 Assignment Operators Examples

### 🔹 1. Basic Assignment (`=`)


x = 10
print(x)   # 10


### 🔹 2. Add and Assign (`+=`)

x = 5
x += 3   # x = x + 3
print(x)  # 8


### 🔹 3. Subtract and Assign (`-=`)


x = 10
x -= 4   # x = x - 4
print(x)  # 6


### 🔹 4. Multiply and Assign (`*=`)


x = 6
x *= 2   # x = x * 2
print(x)  # 12


### 🔹 5. Divide and Assign (`/=`)

x = 10
x /= 2   # x = x / 2
print(x)  # 5.0


### 🔹 6. Floor Divide and Assign (`//=`)


x = 10
x //= 3   # x = x // 3
print(x)  # 3


### 🔹 7. Modulus and Assign (`%=`)

x = 10
x %= 3   # x = x % 3
print(x)  # 1


### 🔹 8. Exponent and Assign (`**=`)

x = 2
x **= 3   # x = x ** 3
print(x)  # 8


### 🔹 9. Bitwise AND Assign (`&=`)

x = 5   # 101
x &= 3  # 011
print(x)  # 1


### 🔹 10. Bitwise OR Assign (`|=`)


x = 5   # 101
x |= 3  # 011
print(x)  # 7


### 🔹 11. Bitwise XOR Assign (`^=`)

x = 5   # 101
x ^= 3  # 011
print(x)  # 6


### 🔹 12. Left Shift Assign (`<<=`)


x = 5   # 101
x <<= 1
print(x)  # 10


### 🔹 13. Right Shift Assign (`>>=`)


x = 5   # 101
x >>= 1
print(x)  # 2


x += 5   # same as x = x + 5

